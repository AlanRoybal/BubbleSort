func bubbleSort (arr: [String]) -> [String] {
    var array = arr
    for _ in 0..<array.count - 1 {
        for j in 0..<array.count - 1 {
            if (array[j] > array[j+1]) {
                let temp = array[j]
                array[j] = array[j+1]
                array[j+1] = temp
            }
        }
    }
    return array
}

